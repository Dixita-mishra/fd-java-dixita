public class Unary {
    public static void main(String[] args) {
        // unary operators
        int a = 5;
        System.out.println("a = " + a);
        
        // unary plus
        System.out.println("+a = " + (+a));
        
        // unary minus
        System.out.println("-a = " + (-a));
        
        // increment operator
        a++;
        System.out.println("After increment, a = " + a);
        
        // decrement operator
        a--;
        System.out.println("After decrement, a = " + a);
    }
}