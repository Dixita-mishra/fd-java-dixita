public class Assignment_op {
    public static void main(String[] args) {
         //assignment operater
        int x = 5;
        x += 3; 
        System.out.println("After += operation, x = " + x);
        x -= 2;
        System.out.println("After -= operation, x = " + x);
        x *= 2;
        System.out.println("After *= operation, x = " + x);
        x /= 4;
        System.out.println("After /= operation, x = " + x);
        x %= 3;
        System.out.println("After %= operation, x = " + x);
    }}
    


        

